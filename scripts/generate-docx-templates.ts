// --- XML будівельники ---

function xmlEscape(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function paragraph(text: string, opts: {
  alignment?: 'left' | 'center' | 'right';
  bold?: boolean;
  size?: number; // half-points (24 = 12pt)
  spacingAfter?: number;
  spacingBefore?: number;
  underline?: boolean;
  italic?: boolean;
  pageBreakBefore?: boolean;
  color?: string;
} = {}): string {
  const {
    alignment = 'left',
    bold = false,
    size = 24,
    spacingAfter = 0,
    spacingBefore = 0,
    underline = false,
    italic = false,
    pageBreakBefore = false,
    color,
  } = opts;

  const jcMap = { left: 'left', center: 'center', right: 'right' };

  let pPr = '<w:pPr>';
  pPr += `<w:jc w:val="${jcMap[alignment]}"/>`;
  pPr += `<w:spacing w:after="${spacingAfter}" w:before="${spacingBefore}"/>`;
  if (pageBreakBefore) {
    pPr += '<w:pageBreakBefore/>';
  }
  pPr += '</w:pPr>';

  let rPr = '<w:rPr>';
  rPr += `<w:sz w:val="${size}"/>`;
  rPr += `<w:szCs w:val="${size}"/>`;
  if (bold) rPr += '<w:b/><w:bCs/>';
  if (italic) rPr += '<w:i/><w:iCs/>';
  if (underline) rPr += '<w:u w:val="single"/>';
  if (color) rPr += `<w:color w:val="${color}"/>`;
  rPr += '</w:rPr>';

  // Розбиваємо текст по \n для підтримки переносів рядків
  const lines = text.split('\n');
  const runs = lines.map((line, i) => {
    let run = `<w:r>${rPr}<w:t xml:space="preserve">${xmlEscape(line)}</w:t></w:r>`;
    if (i < lines.length - 1) {
      run += '<w:r><w:br/></w:r>';
    }
    return run;
  }).join('');

  return `<w:p>${pPr}${runs}</w:p>`;
}

function emptyParagraph(spacingAfter = 0): string {
  return `<w:p><w:pPr><w:spacing w:after="${spacingAfter}"/></w:pPr></w:p>`;
}

