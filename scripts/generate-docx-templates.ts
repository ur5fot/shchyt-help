// --- Генерація ---

function generateTemplate(шаблон: ШаблонДаних): Buffer {
  const paragraphs: string[] = [];

  // Сторінка 1: рапорт/скарга
  paragraphs.push(...buildРапортСторінка(шаблон));

  // Сторінка 2+: підказка
  paragraphs.push(...buildПідказкаСторінка(шаблон.підказка));

  // Сторінка 3: приклад заповнення (якщо є)
  paragraphs.push(...buildПрикладСторінка(шаблон));

  const documentXml = buildDocumentXml(paragraphs);
  return createDocx(documentXml);
}

function main() {
  const outputDir = path.resolve(__dirname, '../client/public/templates/docx');
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  for (const шаблон of шаблони) {
    const buffer = generateTemplate(шаблон);
    const filePath = path.join(outputDir, `${шаблон.id}.docx`);
    fs.writeFileSync(filePath, buffer);
    console.log(`Створено: ${filePath}`);
  }

  console.log(`\nУсього створено ${шаблони.length} шаблонів.`);
}

// Запуск тільки при прямому виконанні (не при імпорті)
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  main();
}

