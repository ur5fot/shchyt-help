function buildContentTypes(): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
  <Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
  <Default Extension="xml" ContentType="application/xml"/>
  <Override PartName="/word/document.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>
  <Override PartName="/word/styles.xml" ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.styles+xml"/>
</Types>`;
}

function buildRels(): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="word/document.xml"/>
</Relationships>`;
}

function buildWordRels(): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/styles" Target="styles.xml"/>
</Relationships>`;
}

function buildStyles(): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:styles xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">
  <w:docDefaults>
    <w:rPrDefault>
      <w:rPr>
        <w:rFonts w:ascii="Times New Roman" w:hAnsi="Times New Roman" w:cs="Times New Roman"/>
        <w:sz w:val="24"/>
        <w:szCs w:val="24"/>
        <w:lang w:val="uk-UA"/>
      </w:rPr>
    </w:rPrDefault>
    <w:pPrDefault>
      <w:pPr>
        <w:spacing w:after="0" w:line="276" w:lineRule="auto"/>
      </w:pPr>
    </w:pPrDefault>
  </w:docDefaults>
</w:styles>`;
}

function createDocx(documentXml: string): Buffer {
  const zip = new PizZip();
  zip.file('[Content_Types].xml', buildContentTypes());
  zip.file('_rels/.rels', buildRels());
  zip.file('word/_rels/document.xml.rels', buildWordRels());
  zip.file('word/document.xml', documentXml);
  zip.file('word/styles.xml', buildStyles());
  return zip.generate({ type: 'nodebuffer' }) as Buffer;
}

// --- Побудова сторінок рапорту ---

function buildРапортСторінка(шаблон: ШаблонДаних): string[] {
  const paragraphs: string[] = [];
  const тип = шаблон.тип === 'рапорт' ? 'РАПОРТ' : 'СКАРГА';

  // Шапка — кому (вирівнювання праворуч)
  for (const рядок of шаблон.кому) {
    paragraphs.push(paragraph(рядок, { alignment: 'right', size: 24 }));
  }

  paragraphs.push(emptyParagraph(120));

  // Від кого
  paragraphs.push(paragraph('{ПОСАДА}', { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('{ЗВАННЯ} {ПІБ}', { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('{ПІДРОЗДІЛ}', { alignment: 'right', size: 24 }));

  paragraphs.push(emptyParagraph(240));

  // Заголовок
  paragraphs.push(paragraph(тип, { alignment: 'center', bold: true, size: 28, spacingAfter: 200 }));

  // Тіло рапорту
  for (const абзац of шаблон.тілоРапорту) {
    paragraphs.push(paragraph(абзац, { size: 24, spacingAfter: 120 }));
  }

  // Облікові документи (якщо є)
  if (шаблон.обліковіДокументи) {
    paragraphs.push(emptyParagraph(120));
    paragraphs.push(paragraph(шаблон.обліковіДокументи, { size: 24, spacingAfter: 120 }));
  }

  // Додатки (якщо є)
  if (шаблон.додатки && шаблон.додатки.length > 0) {
    paragraphs.push(emptyParagraph(120));
    paragraphs.push(paragraph('Додатки:', { bold: true, size: 24, spacingAfter: 60 }));
    for (let i = 0; i < шаблон.додатки.length; i++) {
      paragraphs.push(paragraph(`${i + 1}. ${шаблон.додатки[i]}`, { size: 24, spacingAfter: 40 }));
    }
  }

  paragraphs.push(emptyParagraph(240));

  // Дата і підпис
  paragraphs.push(paragraph('{ПОСАДА}', { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('військової частини {В/Ч}', { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('{ЗВАННЯ}        {Ім\'я ПРІЗВИЩЕ}', { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('{ДАТА}', { alignment: 'left', size: 24 }));

  // Клопотання (якщо є)
  if (шаблон.клопотання && шаблон.клопотання.length > 0) {
    for (const клопотання of шаблон.клопотання) {
      paragraphs.push(emptyParagraph(400));
      paragraphs.push(paragraph(клопотання.кому, { alignment: 'left', bold: true, size: 24, spacingAfter: 120 }));
      paragraphs.push(paragraph('Клопочу по суті рапорту {ЗВАННЯ} {ПІБ}.', { size: 24, spacingAfter: 200 }));
      for (const рядок of клопотання.від) {
        paragraphs.push(paragraph(рядок, { alignment: 'right', size: 24 }));
      }
      paragraphs.push(paragraph('___.___.20__', { alignment: 'left', size: 24 }));
    }
  }

  return paragraphs;
}

function buildПідказкаСторінка(підказка: Підказка): string[] {
  const paragraphs: string[] = [];

  // Заголовок підказки (нова сторінка)
  paragraphs.push(paragraph('ПІДКАЗКА (не для подачі — тільки для вас)', {
    alignment: 'center',
    bold: true,
    size: 26,
    spacingAfter: 200,
    pageBreakBefore: true,
    color: '808080',
  }));

  // Мета рапорту
  paragraphs.push(paragraph('Мета рапорту:', { bold: true, size: 24, spacingAfter: 60 }));
  paragraphs.push(paragraph(підказка.мета, { size: 22, spacingAfter: 200 }));

  // Правова підстава
  paragraphs.push(paragraph('Правова підстава:', { bold: true, size: 24, spacingAfter: 60 }));
  for (const стаття of підказка.правоваПідстава) {
    paragraphs.push(paragraph(`• ${стаття}`, { size: 22, spacingAfter: 60 }));
  }
  paragraphs.push(emptyParagraph(120));

  // Що говорити при подачі
  paragraphs.push(paragraph('Що говорити при подачі:', { bold: true, size: 24, spacingAfter: 60 }));
  paragraphs.push(paragraph(підказка.скриптПодачі, { size: 22, spacingAfter: 200, italic: true }));

  // Якщо не приймають рапорт
  paragraphs.push(paragraph('Якщо не приймають рапорт:', { bold: true, size: 24, spacingAfter: 60 }));
  for (const крок of підказка.якщоНеПриймають) {
    paragraphs.push(paragraph(`• ${крок}`, { size: 22, spacingAfter: 60 }));
  }
  paragraphs.push(emptyParagraph(120));

  // Відповіді на можливі питання
  paragraphs.push(paragraph('Відповіді на можливі питання:', { bold: true, size: 24, spacingAfter: 60 }));
  for (const qa of підказка.відповідіНаПитання) {
    paragraphs.push(paragraph(`— «${qa.питання}»`, { bold: true, size: 22, spacingAfter: 30 }));
    paragraphs.push(paragraph(qa.відповідь, { size: 22, spacingAfter: 120 }));
  }

  return paragraphs;
}

function buildПрикладСторінка(шаблон: ШаблонДаних): string[] {
  const приклад = шаблон.приклад;
  if (!приклад) return [];

  const paragraphs: string[] = [];
  const тип = шаблон.тип === 'рапорт' ? 'РАПОРТ' : 'СКАРГА';

  // Заголовок (нова сторінка)
  paragraphs.push(paragraph('ПРИКЛАД ЗАПОВНЕННЯ (вигадані дані — не подавати!)', {
    alignment: 'center',
    bold: true,
    size: 26,
    spacingAfter: 300,
    pageBreakBefore: true,
    color: '808080',
  }));

  // Шапка — кому
  paragraphs.push(paragraph(приклад.посадаКомандира, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(приклад.званняКомандира, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(приклад.пібКомандира, { alignment: 'right', size: 24 }));

  paragraphs.push(emptyParagraph(120));

  // Від кого
  paragraphs.push(paragraph(приклад.посада, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(`${приклад.звання} ${приклад.піб}`, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(приклад.підрозділ, { alignment: 'right', size: 24 }));

  paragraphs.push(emptyParagraph(240));

  // Заголовок рапорту
  paragraphs.push(paragraph(тип, { alignment: 'center', bold: true, size: 28, spacingAfter: 200 }));

  // Тіло прикладу
  for (const абзац of приклад.тілоПрикладу) {
    paragraphs.push(paragraph(абзац, { size: 24, spacingAfter: 120 }));
  }

  // Додатки (якщо є)
  if (приклад.додатки && приклад.додатки.length > 0) {
    paragraphs.push(emptyParagraph(120));
    paragraphs.push(paragraph('Додатки:', { bold: true, size: 24, spacingAfter: 60 }));
    for (let i = 0; i < приклад.додатки.length; i++) {
      paragraphs.push(paragraph(`${i + 1}. ${приклад.додатки[i]}`, { size: 24, spacingAfter: 40 }));
    }
  }

  paragraphs.push(emptyParagraph(240));

  // Дата і підпис
  paragraphs.push(paragraph(приклад.посада, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(приклад.підрозділ, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph(`${приклад.звання}        ${приклад.піб}`, { alignment: 'right', size: 24 }));
  paragraphs.push(paragraph('01 квітня 2026 р.', { alignment: 'left', size: 24 }));

  return paragraphs;
}

