// Сервіс для виявлення шаблонів документів у відповідях AI

interface ШаблонПатерн {
  id: string;
  ключовіСлова: string[];
}

// Масив паттернів для кожного шаблону документа
// Порядок важливий — перший збіг виграє
const ШАБЛОНИ_ПАТТЕРНИ: ШаблонПатерн[] = [
  {
    id: 'raport-nevyplata',
    // 'грошове забезпечення' навмисно виключено — занадто широке, спрацьовує на будь-яку відповідь про виплати
    ключовіСлова: ['невипла', 'не виплат', 'бойові виплат', 'заборгован', 'затримуван', 'не нарахован', 'не виплачу'],
  },
  {
    id: 'raport-vidpustka',
    ключовіСлова: ['відпустк', 'надати відпустк', 'право на відпустк'],
  },
  {
    id: 'skarga',
    // 'порушен' навмисно виключено — занадто широке, спрацьовує на звичайні правові відповіді
    ключовіСлова: ['оскаржит', 'скаргу', 'неправомірн', 'оскаржен'],
  },
];

/**
 * Визначає який шаблон документа запропонувати на основі тексту відповіді AI.
 * Повертає id шаблону або null якщо жоден не підходить.
 */
export function detectTemplate(text: string): string | null {
  const lower = text.toLowerCase();

  for (const шаблон of ШАБЛОНИ_ПАТТЕРНИ) {
    if (шаблон.ключовіСлова.some((kw) => lower.includes(kw))) {
      return шаблон.id;
    }
  }

  return null;
}

// Експортуємо для тестування
export { ШАБЛОНИ_ПАТТЕРНИ };
