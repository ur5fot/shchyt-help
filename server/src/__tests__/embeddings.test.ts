import { describe, it, expect, beforeAll } from 'vitest';
import {
  створитиЕмбеддинг,
  створитиЕмбеддинги,
  РОЗМІР_ВЕКТОРА,
} from '../services/embeddings';

// Тести завантажують реальну модель — потрібен час на перший запуск
// Модель кешується після першого завантаження

describe('embeddings', () => {
  // Збільшений таймаут для завантаження моделі
  beforeAll(async () => {
    // Прогріваємо модель один раз для всіх тестів
    await створитиЕмбеддинг('тест', 'query');
  }, 120_000);

  describe('створитиЕмбеддинг', () => {
    it('повертає вектор правильної розмірності', async () => {
      const вектор = await створитиЕмбеддинг('грошове забезпечення', 'query');

      expect(вектор).toHaveLength(РОЗМІР_ВЕКТОРА);
      expect(вектор.every((v) => typeof v === 'number')).toBe(true);
    }, 30_000);

    it('генерує різні вектори для різних текстів', async () => {
      const вектор1 = await створитиЕмбеддинг('відпустка військовослужбовця', 'query');
      const вектор2 = await створитиЕмбеддинг('грошове забезпечення', 'query');

      // Вектори мають бути різними
      const однакові = вектор1.every((v, i) => v === вектор2[i]);
      expect(однакові).toBe(false);
    }, 30_000);

    it('додає правильний префікс для query та passage', async () => {
      const queryВектор = await створитиЕмбеддинг('відпустка', 'query');
      const passageВектор = await створитиЕмбеддинг('відпустка', 'passage');

      // query та passage для одного тексту мають давати різні вектори
      // бо модель E5 використовує різні префікси
      const однакові = queryВектор.every((v, i) => v === passageВектор[i]);
      expect(однакові).toBe(false);
    }, 30_000);

    it('повертає нормалізований вектор (довжина ~1)', async () => {
      const вектор = await створитиЕмбеддинг('тест нормалізації', 'query');

      // Обчислюємо L2 норму
      const норма = Math.sqrt(вектор.reduce((сума, v) => сума + v * v, 0));
      expect(норма).toBeCloseTo(1.0, 1);
    }, 30_000);
  });

  describe('створитиЕмбеддинги', () => {
    it('генерує ембеддинги для масиву текстів', async () => {
      const тексти = ['перший текст', 'другий текст', 'третій текст'];
      const вектори = await створитиЕмбеддинги(тексти, 'passage');

      expect(вектори).toHaveLength(3);
      вектори.forEach((вектор) => {
        expect(вектор).toHaveLength(РОЗМІР_ВЕКТОРА);
      });
    }, 60_000);

    it('повертає порожній масив для порожнього вводу', async () => {
      const вектори = await створитиЕмбеддинги([], 'query');
      expect(вектори).toHaveLength(0);
    }, 10_000);
  });
});
