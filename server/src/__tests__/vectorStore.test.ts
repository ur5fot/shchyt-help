// Створити mock ембеддинги (нормалізовані вектори розмірності 384)
function створитиМокЕмбеддинги(кількість: number): number[][] {
  return Array.from({ length: кількість }, (_, і) => {
    const вектор = new Array(384).fill(0);
    // Різні вектори для різних чанків — змінюємо різні компоненти
    вектор[і * 10] = 0.8;
    вектор[і * 10 + 1] = 0.5;
    вектор[і * 10 + 2] = 0.3;
    // Нормалізуємо
    const норма = Math.sqrt(вектор.reduce((с: number, v: number) => с + v * v, 0));
    return вектор.map((v: number) => v / норма);
  });
}

describe('vectorStore', () => {
  let тестоваДиректорія: string;

  beforeEach(() => {
    _скинутиЗєднання();
    тестоваДиректорія = mkdtempSync(join(tmpdir(), 'lancedb-test-'));
  });

  afterEach(() => {
    _скинутиЗєднання();
  });

  describe('ініціалізуватиБД', () => {
    it('створює з\'єднання з БД', async () => {
      const зєднання = await ініціалізуватиБД(тестоваДиректорія);
      expect(зєднання).toBeDefined();
    });

    it('повертає те саме з\'єднання при повторному виклику', async () => {
      const з1 = await ініціалізуватиБД(тестоваДиректорія);
      const з2 = await ініціалізуватиБД(тестоваДиректорія);
      expect(з1).toBe(з2);
    });
  });

  describe('створитиТаблицю', () => {
    it('створює таблицю з чанками та ембеддингами', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      const таблиця = await створитиТаблицю(чанки, ембеддинги);
      expect(таблиця).toBeDefined();

      const рядки = await таблиця.countRows();
      expect(рядки).toBe(3);
    });

    it('кидає помилку при невідповідності кількості чанків та ембеддингів', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(2); // Менше ніж чанків

      await expect(створитиТаблицю(чанки, ембеддинги)).rejects.toThrow(
        'не збігається'
      );
    });

    it('перезаписує існуючу таблицю', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      await створитиТаблицю(чанки, ембеддинги);
      // Скидаємо singleton таблиці щоб відкрити заново
      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      // Створюємо заново з меншою кількістю
      const маліЧанки = чанки.slice(0, 2);
      const маліЕмбеддинги = ембеддинги.slice(0, 2);

      const таблиця = await створитиТаблицю(маліЧанки, маліЕмбеддинги);
      const рядки = await таблиця.countRows();
      expect(рядки).toBe(2);
    });
  });

  describe('пошукПоВектору', () => {
    it('знаходить найближчі чанки за вектором', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      await створитиТаблицю(чанки, ембеддинги);
      // Скидаємо singleton таблиці щоб відкрити заново через пошук
      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      // Шукаємо вектором, схожим на перший чанк
      const результати = await пошукПоВектору(ембеддинги[0], 3);

      expect(результати.length).toBeGreaterThan(0);
      expect(результати.length).toBeLessThanOrEqual(3);

      // Перший результат має бути найближчим до запиту (перший чанк)
      expect(результати[0].id).toBe('test-chunk-1');
      expect(результати[0]._distance).toBeDefined();
      expect(typeof результати[0]._distance).toBe('number');
    });

    it('повертає правильну структуру результатів', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      await створитиТаблицю(чанки, ембеддинги);
      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      const результати = await пошукПоВектору(ембеддинги[0], 1);

      expect(результати[0]).toMatchObject({
        id: 'test-chunk-1',
        article: 'Стаття 1',
        part: 'Частина 1',
        title: 'Право на відпустку',
        lawTitle: 'Про соціальний і правовий захист',
        sourceUrl: 'https://zakon.rada.gov.ua/laws/show/test1',
      });
      expect(результати[0].keywords).toEqual(['відпустка', 'щорічна', 'право']);
    });

    it('кидає помилку якщо таблиця не існує', async () => {
      await ініціалізуватиБД(тестоваДиректорія);

      await expect(пошукПоВектору(new Array(384).fill(0), 5)).rejects.toThrow(
        'не ініціалізована'
      );
    });

    it('обмежує кількість результатів через topK', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      await створитиТаблицю(чанки, ембеддинги);
      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      const результати = await пошукПоВектору(ембеддинги[0], 1);
      expect(результати).toHaveLength(1);
    });
  });

  describe('оновитиЧанки', () => {
    it('створює таблицю якщо вона не існує', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      await оновитиЧанки(чанки, ембеддинги);

      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      // Перевіряємо що дані доступні для пошуку
      const результати = await пошукПоВектору(ембеддинги[0], 3);
      expect(результати.length).toBeGreaterThan(0);
    });

    it('додає нові чанки до існуючої таблиці', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();
      const ембеддинги = створитиМокЕмбеддинги(чанки.length);

      // Спочатку створюємо з 2 чанками
      await створитиТаблицю(чанки.slice(0, 2), ембеддинги.slice(0, 2));

      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      // Оновлюємо — додаємо третій чанк
      const новийЧанк: LawChunk = {
        id: 'test-chunk-new',
        article: 'Стаття 99',
        part: 'Частина 1',
        title: 'Нова стаття',
        text: 'Текст нової статті.',
        keywords: ['новий'],
        lawTitle: 'Новий закон',
        sourceUrl: 'https://example.com',
      };
      const новийЕмбеддинг = створитиМокЕмбеддинги(1);

      await оновитиЧанки([новийЧанк], новийЕмбеддинг);

      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      // Пошук по новому ембеддингу має знайти новий чанк
      const результати = await пошукПоВектору(новийЕмбеддинг[0], 10);
      const ids = результати.map((р) => р.id);
      expect(ids).toContain('test-chunk-new');
    });

    it('кидає помилку при невідповідності кількості', async () => {
      await ініціалізуватиБД(тестоваДиректорія);
      const чанки = створитиТестовіЧанки();

      // Створюємо таблицю
      await створитиТаблицю(чанки, створитиМокЕмбеддинги(чанки.length));

      _скинутиЗєднання();
      await ініціалізуватиБД(тестоваДиректорія);

      await expect(оновитиЧанки(чанки, створитиМокЕмбеддинги(1))).rejects.toThrow(
        'не збігається'
      );
    });
  });
});
