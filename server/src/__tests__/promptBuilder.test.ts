import { describe, it, expect } from 'vitest';
import { buildPrompt } from '../services/promptBuilder';
import type { LawChunk } from '../../../laws/index';

// Тестові чанки
const чанкЗЧастиною: LawChunk = {
  id: 'test-st9-ch1',
  article: 'Стаття 9',
  part: 'Частина 1',
  title: 'Грошове забезпечення',
  text: 'Держава забезпечує військовослужбовцям достатній рівень грошового забезпечення.',
  keywords: ['грошове забезпечення', 'виплати'],
  lawTitle: 'Закон про соцзахист військовослужбовців',
  sourceUrl: 'https://zakon.rada.gov.ua/laws/show/2011-12',
};

const чанкБезЧастини: LawChunk = {
  id: 'test-st10-1',
  article: 'Стаття 10-1',
  part: '',
  title: 'Відпустки',
  text: 'В особливий період військовослужбовцям надаються відпустки тривалістю 90 днів.',
  keywords: ['відпустка', '90 днів'],
  lawTitle: 'Закон про соцзахист військовослужбовців',
  sourceUrl: 'https://zakon.rada.gov.ua/laws/show/2011-12',
};

const чанкБезНазви: LawChunk = {
  id: 'test-st9-ch3',
  article: 'Стаття 9',
  part: 'Частина 3',
  text: 'Грошове забезпечення виплачується у розмірах, визначених Кабміном.',
  keywords: ['розмір виплат', 'кабмін'],
  lawTitle: 'Закон про соцзахист військовослужбовців',
  sourceUrl: 'https://zakon.rada.gov.ua/laws/show/2011-12',
};

describe('buildPrompt', () => {
  it('містить питання користувача', () => {
    const результат = buildPrompt('Коли я маю право на відпустку?', [чанкЗЧастиною]);
    expect(результат).toContain('Коли я маю право на відпустку?');
  });

  it('містить назву закону', () => {
    const результат = buildPrompt('питання', [чанкЗЧастиною]);
    expect(результат).toContain('Закон про соцзахист військовослужбовців');
  });

  it('включає статтю та частину коли частина є', () => {
    const результат = buildPrompt('питання', [чанкЗЧастиною]);
    expect(результат).toContain('Стаття 9');
    expect(результат).toContain('Частина 1');
  });

  it('включає статтю без частини коли частина порожня', () => {
    const результат = buildPrompt('питання', [чанкБезЧастини]);
    expect(результат).toContain('Стаття 10-1');
    // Порожня частина не повинна додаватися зайвими розділювачами
    expect(результат).not.toContain('Стаття 10-1, ');
  });

  it('включає текст чанку', () => {
    const результат = buildPrompt('питання', [чанкЗЧастиною]);
    expect(результат).toContain('Держава забезпечує військовослужбовцям');
  });

  it('обробляє кілька чанків', () => {
    const результат = buildPrompt('питання', [чанкЗЧастиною, чанкБезЧастини]);
    expect(результат).toContain('Стаття 9');
    expect(результат).toContain('Стаття 10-1');
    expect(результат).toContain('Держава забезпечує');
    expect(результат).toContain('В особливий період');
  });

  it('обробляє чанк без title', () => {
    const результат = buildPrompt('питання', [чанкБезНазви]);
    expect(результат).toContain('Стаття 9');
    expect(результат).toContain('Частина 3');
    // Не має викинути помилку — просто без заголовку
  });

  it('повертає порожній контекст коли чанків немає', () => {
    const результат = buildPrompt('питання', []);
    expect(результат).toContain('питання');
    // Немає чанків — контекст порожній або відсутній
    expect(результат).not.toContain('📎');
  });

  it('структура промпту: спочатку контекст, потім питання', () => {
    const результат = buildPrompt('моє питання', [чанкЗЧастиною]);
    const позиціяКонтексту = результат.indexOf('Закон про соцзахист');
    const позиціяПитання = результат.indexOf('моє питання');
    expect(позиціяКонтексту).toBeLessThan(позиціяПитання);
  });

  it('розділяє кілька чанків роздільником', () => {
    const результат = buildPrompt('питання', [чанкЗЧастиною, чанкБезЧастини]);
    // Має бути якийсь розділювач між чанками
    expect(результат).toContain('---');
  });
});
