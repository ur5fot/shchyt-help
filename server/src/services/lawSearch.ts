import type { LawChunk } from '../../../laws/index';

export interface SearchResult {
  chunk: LawChunk;
  score: number;
}

// Мінімальна довжина слова для пошуку — слова ≤2 символи ігноруються
// Значення 3 дозволяє шукати за військовими абревіатурами: УБД, ВЛК, ЗСУ тощо
const МІН_ДОВЖИНА_СЛОВА = 3;

// Вагові коефіцієнти пошуку
const ВАГА_KEYWORD = 3;
const ВАГА_НАЗВИ = 2;
const ВАГА_ТЕКСТУ = 1;

// Мінімальна оцінка релевантності — результати з нижчою оцінкою відфільтровуються
const МІНІМАЛЬНА_ОЦІНКА = 3;

// Максимальна кількість результатів
const МАКС_РЕЗУЛЬТАТІВ = 5;

// Типові українські закінчення для базової нормалізації слів
// Сортовані за довжиною (від довших до коротших) — щоб "ування" видалялось раніше за "ння"
const ЗАКІНЧЕННЯ = [
  'ування', 'ення', 'ання', 'ість',
  'ями', 'ють', 'ити', 'ати', 'ути', 'ого', 'ому',
  'ою', 'ів', 'ам', 'ях', 'ій', 'ся', 'ти', 'їх', 'ні',
];

// Мінімальна довжина основи після видалення закінчення
const МІН_ДОВЖИНА_ОСНОВИ = 3;

/**
 * Карта синонімів для частих військових запитів.
 * Ключ — слово з запиту, значення — додаткові слова для пошуку.
 * Синоніми двосторонні: якщо "зарплата" → "грошове забезпечення",
 * то й "грошове" → "зарплата" тощо.
 */
const СИНОНІМИ: Record<string, string[]> = {
  'зарплата': ['грошове', 'забезпечення', 'оклад', 'виплати', 'виплата'],
  'зарплату': ['грошове', 'забезпечення', 'оклад', 'виплати'],
  'грошове': ['зарплата', 'оклад', 'виплати'],
  'оклад': ['грошове', 'забезпечення', 'зарплата', 'виплати'],
  'бойові': ['додаткова', 'винагорода', 'бойова'],
  'бойова': ['додаткова', 'винагорода', 'бойові'],
  'винагорода': ['бойові', 'бойова', 'додаткова'],
  'мобілізація': ['призов', 'військовий', 'обов\'язок', 'мобілізований'],
  'призов': ['мобілізація', 'військовий', 'обов\'язок'],
  'демобілізація': ['звільнення', 'служба', 'припинення'],
  'звільнення': ['демобілізація', 'припинення', 'служба'],
  'поранення': ['травма', 'контузія', 'інвалідність', 'лікування'],
  'травма': ['поранення', 'контузія', 'інвалідність', 'лікування'],
  'контузія': ['поранення', 'травма', 'лікування'],
  'лікування': ['медицина', 'госпіталь', 'медичне'],
  'госпіталь': ['лікування', 'медицина', 'медичне'],
  'медицина': ['лікування', 'госпіталь', 'медичне'],
  'пільги': ['знижка', 'компенсація', 'соціальний', 'захист'],
  'компенсація': ['пільги', 'виплата', 'відшкодування'],
  'переведення': ['переміщення', 'ротація', 'служба'],
  'ротація': ['переведення', 'переміщення'],
  'відпустка': ['відпочинок', 'щорічна'],
  'відпочинок': ['відпустка', 'щорічна'],
};

/**
 * Базова нормалізація українського слова — видалення типових закінчень.
 * Повертає основу слова або оригінал, якщо закінчення не знайдено
 * або основа занадто коротка.
 */
export function нормалізувати(слово: string): string {
  for (const закінчення of ЗАКІНЧЕННЯ) {
    if (слово.endsWith(закінчення) && слово.length - закінчення.length >= МІН_ДОВЖИНА_ОСНОВИ) {
      return слово.slice(0, -закінчення.length);
    }
  }
  return слово;
}

/**
 * Розширює масив слів запиту синонімами з карти СИНОНІМИ.
 * Повертає новий масив з оригінальними словами та їхніми синонімами (без дублікатів).
 */
export function розширитиСинонімами(слова: string[]): string[] {
  const набір = new Set(слова);
  for (const слово of слова) {
    const синоніми = СИНОНІМИ[слово];
    if (синоніми) {
      for (const с of синоніми) {
        if (с.length >= МІН_ДОВЖИНА_СЛОВА) {
          набір.add(с);
        }
      }
    }
  }
  return Array.from(набір);
}

/**
 * Пошук релевантних чанків законів за запитом користувача.
 * Використовує нормалізацію слів, синоніми та вагові коефіцієнти:
 * keywords (+3), назва статті (+2), текст (+1).
 * Повертає відсортований масив результатів (не більше 5).
 */
export function searchLaws(запит: string, чанки: LawChunk[]): SearchResult[] {
  if (!запит || запит.trim().length === 0) {
    return [];
  }

  // Розбиваємо запит на слова та фільтруємо короткі
  // Знімаємо пунктуацію перед розбивкою, щоб "відпустка?" збігалось з "відпустка"
  const оригінальніСлова = запит
    .toLowerCase()
    .replace(/[.,;:!?()«»"']/g, ' ')
    .split(/\s+/)
    .filter(с => с.length >= МІН_ДОВЖИНА_СЛОВА);

  if (оригінальніСлова.length === 0) {
    return [];
  }

  // Розширюємо запит синонімами та нормалізованими формами
  const словаЗСинонімами = розширитиСинонімами(оригінальніСлова);
  const нормалізовані = оригінальніСлова.map(нормалізувати);
  const всіСлова = Array.from(new Set([...словаЗСинонімами, ...нормалізовані]));

  const результати: SearchResult[] = [];

  for (const чанк of чанки) {
    let оцінка = 0;

    const keywordsНижній = чанк.keywords.map(k => k.toLowerCase());
    const текстНижній = чанк.text.toLowerCase();
    const назваНижня = (чанк.title ?? '').toLowerCase();

    for (const слово of всіСлова) {
      // Перевіряємо keywords (вага +3 за кожен збіг)
      if (keywordsНижній.some(k => k.includes(слово))) {
        оцінка += ВАГА_KEYWORD;
      }

      // Перевіряємо назву статті (вага +2)
      if (назваНижня.includes(слово)) {
        оцінка += ВАГА_НАЗВИ;
      }

      // Перевіряємо текст статті (вага +1)
      if (текстНижній.includes(слово)) {
        оцінка += ВАГА_ТЕКСТУ;
      }
    }

    if (оцінка >= МІНІМАЛЬНА_ОЦІНКА) {
      результати.push({ chunk: чанк, score: оцінка });
    }
  }

  // Сортуємо за спаданням оцінки та обмежуємо кількість
  результати.sort((а, б) => б.score - а.score);
  return результати.slice(0, МАКС_РЕЗУЛЬТАТІВ);
}
