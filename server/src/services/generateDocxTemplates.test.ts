import { describe, it, expect } from 'vitest';
import {
  buildПрикладСторінка,
  шаблони,
  generateTemplate,
  xmlEscape,
  paragraph,
} from '../../../scripts/generate-docx-templates';
import type { ШаблонДаних, ПрикладДаних } from '../../../scripts/generate-docx-templates';

describe('generate-docx-templates', () => {
  describe('інтерфейс ПрикладДаних', () => {
    it('приклад має всі обов\'язкові поля', () => {
      const приклад: ПрикладДаних = {
        пібКомандира: 'ПЕТРЕНКО Іван Іванович',
        званняКомандира: 'полковник',
        посадаКомандира: 'Командир військової частини А0001',
        піб: 'КОВАЛЕНКО Олександр Сергійович',
        звання: 'солдат',
        посада: 'стрілець',
        підрозділ: '1 механізована рота',
        тілоПрикладу: ['Перший абзац тіла прикладу.', 'Другий абзац.'],
      };

      expect(приклад.пібКомандира).toBe('ПЕТРЕНКО Іван Іванович');
      expect(приклад.званняКомандира).toBe('полковник');
      expect(приклад.посадаКомандира).toBe('Командир військової частини А0001');
      expect(приклад.піб).toBe('КОВАЛЕНКО Олександр Сергійович');
      expect(приклад.звання).toBe('солдат');
      expect(приклад.посада).toBe('стрілець');
      expect(приклад.підрозділ).toBe('1 механізована рота');
      expect(приклад.тілоПрикладу).toHaveLength(2);
    });

    it('приклад може мати додатки', () => {
      const приклад: ПрикладДаних = {
        пібКомандира: 'ПЕТРЕНКО Іван Іванович',
        званняКомандира: 'полковник',
        посадаКомандира: 'Командир військової частини А0001',
        піб: 'КОВАЛЕНКО Олександр Сергійович',
        звання: 'солдат',
        посада: 'стрілець',
        підрозділ: '1 механізована рота',
        тілоПрикладу: ['Текст.'],
        додатки: ['Копія паспорта', 'Копія ІПН'],
      };

      expect(приклад.додатки).toHaveLength(2);
    });
  });

  describe('buildПрикладСторінка', () => {
    it('повертає порожній масив якщо приклад відсутній', () => {
      const шаблон: ШаблонДаних = {
        id: 'test',
        тип: 'рапорт',
        кому: ['Командиру'],
        тілоРапорту: ['Прошу...'],
        підказка: {
          мета: 'тест',
          правоваПідстава: [],
          скриптПодачі: 'тест',
          якщоНеПриймають: [],
          відповідіНаПитання: [],
        },
      };

      const result = buildПрикладСторінка(шаблон);
      expect(result).toEqual([]);
    });

    it('генерує XML з заголовком прикладу сірим кольором', () => {
      const шаблон: ШаблонДаних = {
        id: 'test',
        тип: 'рапорт',
        кому: ['Командиру'],
        тілоРапорту: ['Прошу...'],
        підказка: {
          мета: 'тест',
          правоваПідстава: [],
          скриптПодачі: 'тест',
          якщоНеПриймають: [],
          відповідіНаПитання: [],
        },
        приклад: {
          пібКомандира: 'ПЕТРЕНКО І.І.',
          званняКомандира: 'полковник',
          посадаКомандира: 'Командир в/ч А0001',
          піб: 'КОВАЛЕНКО О.С.',
          звання: 'солдат',
          посада: 'стрілець',
          підрозділ: '1 мех. рота',
          тілоПрикладу: ['Текст прикладу.'],
        },
      };

      const result = buildПрикладСторінка(шаблон);
      expect(result.length).toBeGreaterThan(0);

      const xml = result.join('');

      // Заголовок з сірим кольором
      expect(xml).toContain('ПРИКЛАД ЗАПОВНЕННЯ');
      expect(xml).toContain('808080');
      expect(xml).toContain('w:pageBreakBefore');

      // Дані командира
      expect(xml).toContain('ПЕТРЕНКО');
      expect(xml).toContain('полковник');

      // Дані автора
      expect(xml).toContain('КОВАЛЕНКО');
      expect(xml).toContain('солдат');

      // Заголовок РАПОРТ
      expect(xml).toContain('РАПОРТ');

      // Тіло прикладу
      expect(xml).toContain('Текст прикладу');
    });

    it('генерує XML для скарги з заголовком СКАРГА', () => {
      const шаблон: ШаблонДаних = {
        id: 'test-skarga',
        тип: 'скарга',
        кому: ['Начальнику'],
        тілоРапорту: ['Скаржусь...'],
        підказка: {
          мета: 'тест',
          правоваПідстава: [],
          скриптПодачі: 'тест',
          якщоНеПриймають: [],
          відповідіНаПитання: [],
        },
        приклад: {
          пібКомандира: 'ІВАНЕНКО П.П.',
          званняКомандира: 'генерал-майор',
          посадаКомандира: 'Начальник',
          піб: 'СИДОРЕНКО А.В.',
          звання: 'сержант',
          посада: 'командир відділення',
          підрозділ: '2 взвод',
          тілоПрикладу: ['Текст скарги.'],
        },
      };

      const result = buildПрикладСторінка(шаблон);
      const xml = result.join('');
      expect(xml).toContain('СКАРГА');
    });

    it('включає додатки якщо вони є в прикладі', () => {
      const шаблон: ШаблонДаних = {
        id: 'test',
        тип: 'рапорт',
        кому: ['Командиру'],
        тілоРапорту: ['Прошу...'],
        підказка: {
          мета: 'тест',
          правоваПідстава: [],
          скриптПодачі: 'тест',
          якщоНеПриймають: [],
          відповідіНаПитання: [],
        },
        приклад: {
          пібКомандира: 'ПЕТРЕНКО І.І.',
          званняКомандира: 'полковник',
          посадаКомандира: 'Командир в/ч А0001',
          піб: 'КОВАЛЕНКО О.С.',
          звання: 'солдат',
          посада: 'стрілець',
          підрозділ: '1 мех. рота',
          тілоПрикладу: ['Текст.'],
          додатки: ['Копія паспорта', 'Копія ІПН'],
        },
      };

      const result = buildПрикладСторінка(шаблон);
      const xml = result.join('');
      expect(xml).toContain('Додатки:');
      expect(xml).toContain('Копія паспорта');
      expect(xml).toContain('Копія ІПН');
    });
  });

  describe('ШаблонДаних з прикладом', () => {
    it('всі існуючі шаблони є валідними (зворотна сумісність)', () => {
      expect(шаблони).toHaveLength(6);
      for (const шаблон of шаблони) {
        expect(шаблон.id).toBeTruthy();
        expect(шаблон.тип).toMatch(/^(рапорт|скарга)$/);
        expect(шаблон.кому.length).toBeGreaterThan(0);
        expect(шаблон.тілоРапорту.length).toBeGreaterThan(0);
        expect(шаблон.підказка).toBeDefined();
      }
    });

    it('generateTemplate працює для шаблонів без прикладу', () => {
      const buffer = generateTemplate(шаблони[0]);
      expect(buffer).toBeInstanceOf(Buffer);
      expect(buffer.length).toBeGreaterThan(0);
    });

    it('generateTemplate працює для шаблону з прикладом', () => {
      const шаблонЗПрикладом: ШаблонДаних = {
        ...шаблони[0],
        приклад: {
          пібКомандира: 'ПЕТРЕНКО І.І.',
          званняКомандира: 'полковник',
          посадаКомандира: 'Командир в/ч А0001',
          піб: 'КОВАЛЕНКО О.С.',
          звання: 'солдат',
          посада: 'стрілець',
          підрозділ: '1 мех. рота',
          тілоПрикладу: ['Тестовий текст.'],
          додатки: ['Копія паспорта'],
        },
      };

      const buffer = generateTemplate(шаблонЗПрикладом);
      expect(buffer).toBeInstanceOf(Buffer);
      expect(buffer.length).toBeGreaterThan(0);
    });
  });

  describe('xmlEscape', () => {
    it('екранує спецсимволи XML', () => {
      expect(xmlEscape('тест & <перевірка>')).toBe('тест &amp; &lt;перевірка&gt;');
    });
  });
});
